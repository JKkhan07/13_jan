public class BoxingOpration {

	public static void main(String[] args) {
		int age=20;
		Integer a=new Integer(age);
		System.out.println("value of a: "+a);
		System.out.println("vaule of age: "+age);
		System.out.println("age==a: "+(age==a));
		Integer b=new Integer(60);
		System.out.println("vaule of b: "+b);
		Integer c=30;
		System.out.println("vaule of c: "+c);
	}

}
