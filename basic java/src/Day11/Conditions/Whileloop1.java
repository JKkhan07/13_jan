package Day11.Conditions;

public class Whileloop1 {

	public static void main(String[] args) {
		int i = 0;
		while (i < 4) {
			System.out.println("increment : " + i);
			i++;

		}
		int c1 = 4;
		while (c1 > 0) {
			System.out.println("decrement :" + c1);
			c1--;
		}
		char x1 = 'a';
		while (x1 < 'z') {
			System.out.print(x1);
			x1++;

		}
		char z1 = 'z';
		while (z1 > 'a') {
			System.out.println(z1);
			--z1;
		}

	}
}
