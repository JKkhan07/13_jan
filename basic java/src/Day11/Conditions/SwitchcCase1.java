package Day11.Conditions;

public class SwitchcCase1 {

	public static void main(String[] args) {
		int month = 1;
		switch (month) {
		case 1:
			System.out.println("this month is january");
			break;
		case 2:
			System.out.println("this month is february");
			break;
		case 3:
			System.out.println("this month is march");
			break;
		case 4:
			System.out.println("this month is april");
			break;
		default:
			System.out.println("don't match with any case");
			break;
		}

	}

}
