package Day17.Operators;

public class ArithmeticOp {

	public static void main(String[] args) {
		int number1 = 40, number2 = 30;
		//For Addition
		int res = number1 + number2;
		System.out.println("Addition = " + res);
		// For subtraction
		int res1 = number1 - number2;
		System.out.println("subtraction = " + res1);
		// For division
		double res2 = number1 / number2;
		System.out.println("Division = " + res2);
		// For Multiplication
		int res3= number1*number2;
		System.out.println("Multiplication "+res3);
		// For modulus
		double res4=number1%number2;
		System.out.println("Modulus ="+res4);
		

	}

}
